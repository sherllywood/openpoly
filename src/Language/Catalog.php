	/**
	 * Look up a preset entry by its code.
	 *
	 * @param string $code Language code, e.g. "zh_CN".
	 * @return array<string, string|int>|null
	 */
	public static function find( string $code ): ?array {
		foreach ( self::all() as $entry ) {
			if ( $entry['code'] === $code ) {
				return $entry;
			}
		}
		return null;
	}

	/**
	 * Return the codes that are RTL scripts.
	 *
	 * Used to set text_direction=1 in the DB row.
	 *
	 * @return array<int, string>
	 */
	public static function rtl_codes(): array {
		$out = array();
		foreach ( self::all() as $entry ) {
			if ( 1 === (int) $entry['direction'] ) {
				$out[] = (string) $entry['code'];
			}
		}
		return $out;
	}
